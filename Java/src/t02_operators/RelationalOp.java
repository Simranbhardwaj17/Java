package t02_operators;

public class RelationalOp {

	public static void main(String[] args) {
		
		/*
		 * returns bpplean val only - T/F
		 a = 3;
		 b = 5;
		               
		a == b;   -> returns F
		a != b;   -> returns T 
		a < b;    -> returns F
		a > b;   -> returns T 
		a <= b;   -> returns F
		a >= b;   -> returns T 
		
		*/
		
		int hisAge = 20;
		int herAge = 20;
		
		System.out.println(hisAge == herAge);
		System.out.println(hisAge >= herAge);
		System.out.println(hisAge <= herAge);
		System.out.println(hisAge > herAge);
		

	}

}
